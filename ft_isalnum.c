int	ft_isalnum(int c)
{
	if ((c > 64 && c < 91)
		|| (c > 96 && c < 123)
		|| (c > 47 && c < 58)
		|| (c == 262) || (c == 266) || (c == 272) || (c == 274)
		|| (c == 280) || (c == 286) || (c == 290) || (c == 302)
		|| (c == 308) || (c == 310) || (c == 320) || (c == 334)
		|| (c == 348) || (c == 366) || (c == 388) || (c == 390)
		|| (c == 396) || (c == 398) || (c == 406) || (c == 412)
		|| (c == 420) || (c == 434) || (c == 436) || (c == 442)
		|| (c == 444) || (c == 450) || (c == 452) || (c == 458)
		|| (c == 460) || (c == 466) || (c == 468) || (c == 474)
		|| (c == 476) || (c == 484) || (c == 492) || (c == 500)
		|| (c == 512))
		return (8);
	return (0);
}
