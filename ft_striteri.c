#include "libft.h"

void	ft_striteri(char *str, void (*f)(unsigned int, char *))
{
	int	i;

	if (!str)
		return ;
	i = 0;
	while (str[i] != '\0')
	{
		(*f)(i, &str[i]);
		i++;
	}
	str[i] = '\0';
}
